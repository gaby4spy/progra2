public class OperadoresComparacion {

    public static void main(String[] args) {

        int a=3,b=2;

        // igualdad ==
        var resultado = a==b;
        System.out.println(resultado);

        // disnto !=

        resultado= a!=b;
        System.out.println(resultado);


        System.out.println();
    }
}
