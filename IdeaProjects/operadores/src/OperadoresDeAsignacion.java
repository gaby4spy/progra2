public class OperadoresDeAsignacion {
    public static void main(String[] args) {

        /// Asignacion =

        var miNumero= 10;
        int miNumero2;
        miNumero2=15;

        /// asignacion compuesto

        //+=
        miNumero2 +=5;
        /*
        * podemos usar estos operadores de asignacion compuesto combinanlo de
        * difentes formas  ejemplo: */

        // -=,*=,/= y %=

        miNumero2 *=2;
        System.out.println(miNumero2);

        //Asignacion de variables multiples

        int a=10,b=15,c=20;
        System.out.printf("a=%d ,b=%d,c=%d",a,b,c);


    }
}
